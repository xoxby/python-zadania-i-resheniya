import re

# --- Задача 1 ---
# Найди все числа в строке:
# s = "В 2026 году я прошёл 15 уроков и решил 200 задач"
# Используй re.findall(r"\d+", s)
# Твой код:





s = "В 2026 году я прошёл 15 уроков и решил 200 задач"
print(re.findall(r"\d+", s))

# --- Задача 2 ---
# Найди все слова (последовательности букв) в строке:
# s = "Hello, World! I am 20 years old."
# Используй \w+
# Твой код:





s = "Hello, World! I am 20 years old."
print(re.findall(r"\w+", s))

# --- Задача 3 ---
# Замени несколько пробелов подряд на один дефис:
# s = "один   два     три  четыре"  →  "один-два-три-четыре"
# Используй re.sub(r"\s+", "-", s)
# Твой код:





s = "один   два     три  четыре"
print(re.sub(r"\s+", "-", s))

# --- Задача 4 ---
# Проверь, является ли строка валидным логином:
# Правило: ^[\w-]{3,16}$  (буквы, цифры, _, -, длина 3-16)
# Протестируй: "Python_master", "@invalid", "ab", "valid-user-123"
# Твой код:





def ok_login(name: str) -> bool:
    return bool(re.fullmatch(r"[\w-]{3,16}", name))


for x in ("Python_master", "@invalid", "ab", "valid-user-123"):
    print(x, ok_login(x))

# --- Задача 5 ---
# Найди все email-адреса в тексте:
# text = "Пишите на ivan@gmail.com или anna@mail.ru для связи"
# Шаблон: [a-z._-]+@[a-z._-]+
# Твой код:





text = "Пишите на ivan@gmail.com или anna@mail.ru для связи"
print(re.findall(r"[a-z._-]+@[a-z._-]+", text))

# --- Задача 6 ---
# Найди все даты в формате ДД.ММ.ГГГГ или ДД-ММ-ГГГГ:
# text = "Родился 05-03-1987, женился 15.06.2015"
# Шаблон: \d{2}[.-]\d{2}[.-]\d{4}
# Твой код:





text = "Родился 05-03-1987, женился 15.06.2015"
print(re.findall(r"\d{2}[.-]\d{2}[.-]\d{4}", text))

# --- Задача 7 ---
# Найди все фрагменты в квадратных скобках (ленивый квантификатор):
# text = "Текст [1] и ещё [важно] и [16][17]"
# Используй \[.*?\]
# Твой код:





text = "Текст [1] и ещё [важно] и [16][17]"
print(re.findall(r"\[.*?\]", text))

# --- Задача 8 ---
# Разбей строку по запятым И точкам с запятой:
# s = "one,two;three,four;five"
# Используй re.split(r"[,;]", s)
# Твой код:





s = "one,two;three,four;five"
print(re.split(r"[,;]", s))

# --- Задача 9 ---
# Найди "python" в тексте НЕЗАВИСИМО от регистра:
# text = "Python, python, PYTHON — всё один язык"
# Используй флаг re.IGNORECASE (или re.I)
# Твой код:





text = "Python, python, PYTHON — всё один язык"
print(re.findall(r"python", text, flags=re.I))

# --- Задача 10 ---
# Найди все телефоны в формате +7XXXXXXXXXX:
# text = "+7 499 456-45-78, +74994564578, 7(499) 456 45 78"
# Шаблон упрощённый: \+?7\d{10}
# Твой код:





text = "+7 499 456-45-78, +74994564578, 7(499) 456 45 78"
phones = []
for chunk in text.split(","):
    digits = re.sub(r"\D", "", chunk)
    if len(digits) == 11 and digits.startswith("7"):
        phones.append("+" + digits)
print(phones)

# --- Задача 11 ---
# Удали все HTML-теги из строки:
# html = "<p>Текст <b>жирный</b> и <i>курсив</i></p>"
# Используй re.sub(r"<.*?>", "", html)
# Результат: "Текст жирный и курсив"
# Твой код:





html = "<p>Текст <b>жирный</b> и <i>курсив</i></p>"
print(re.sub(r"<.*?>", "", html))

# --- Задача 12 ---
# Извлеки домен из URL:
# url = "https://www.python.org/docs"
# Подсказка: разбей по / через split или найди через regex.
# Твой код:





url = "https://www.python.org/docs"
print(url.split("//", 1)[1].split("/", 1)[0])
