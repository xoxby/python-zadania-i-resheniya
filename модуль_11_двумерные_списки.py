m = [[1, 2, 3], [4, 5, 6]]
t = [[m[i][j] for i in range(len(m))] for j in range(len(m[0]))]
print(t)

# --- Задача 8 ---
# Создай "поле" крестиков-ноликов 3x3 (список списков).
# Заполни его "-". Поставь "X" в центр и "O" в угол. Выведи поле.
# Твой код:





field = [["-" for _ in range(3)] for _ in range(3)]
field[1][1] = "X"
field[0][0] = "O"
for row in field:
    print(" ".join(row))
