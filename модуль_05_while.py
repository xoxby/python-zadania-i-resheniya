# --- Задача 1 ---
# Выведи числа от 1 до 10 через while.
# Твой код:





i = 1
while i <= 10:
    print(i)
    i += 1

# --- Задача 2 ---
# Выведи числа от 10 до 1 (обратный отсчёт).
# Твой код:





i = 10
while i >= 1:
    print(i)
    i -= 1

# --- Задача 3 ---
# Посчитай сумму чисел от 1 до n (n спроси у пользователя).
# Твой код:





n = int(input("n: "))
s = 0
i = 1
while i <= n:
    s += i
    i += 1
print(s)

# --- Задача 4 ---
# Выведи только чётные числа от 1 до 20.
# Твой код:





i = 1
while i <= 20:
    if i % 2 == 0:
        print(i)
    i += 1

# --- Задача 5 ---
# Найди факториал числа n (n! = 1*2*3*...*n) через while.
# Твой код:





n = int(input("n для факториала: "))
res = 1
i = 2
while i <= n:
    res *= i
    i += 1
print(res)

# --- Задача 6 ---
# Собирай числа от пользователя, пока не введёт 0.
# В конце выведи их произведение.
# Используй while True + break.
# Твой код:





prod = 1
while True:
    x = int(input("Число (0 — стоп): "))
    if x == 0:
        break
    prod *= x
print("Произведение:", prod)

# --- Задача 7 ---
# Выведи числа от 0 до 9, но пропусти число 3 (continue)
# и останови цикл на 7 (break).
# Твой код:





i = 0
while i <= 9:
    if i == 3:
        i += 1
        continue
    print(i, end=" ")
    if i == 7:
        break
    i += 1
print()

# --- Задача 8 ---
# Угадай число: загадай число (например, secret = 7).
# Проси пользователя угадать. Говори "больше" или "меньше".
# Считай количество попыток.
# Твой код:





secret = 7
tries = 0
while True:
    g = int(input("Угадай (1..10): "))
    tries += 1
    if g < secret:
        print("больше")
    elif g > secret:
        print("меньше")
    else:
        print("Угадал за", tries, "попыток")
        break

# --- Задача 9 ---
# Выведи все делители числа n через while.
# Пример: n = 12 → 1 2 3 4 6 12
# Твой код:





n = int(input("n: "))
d = 1
while d <= n:
    if n % d == 0:
        print(d, end=" ")
    d += 1
print()

# --- Задача 10 ---
# Найди сумму цифр числа.
# Пример: 4321 → 4 + 3 + 2 + 1 = 10.
# Используй % 10 и // 10 в цикле.
# Твой код:





n = int(input("Число: "))
s = 0
while n > 0:
    s += n % 10
    n //= 10
print(s)

# --- Задача 11 ---
# Меню: while True с пунктами:
# 1. Привет
# 2. Текущее время (просто print текст)
# 3. Выход
# При выборе 3 — break. При неверном вводе — "Нет такого пункта".
# Твой код:





while True:
    print("1. Привет\n2. Время\n3. Выход")
    c = input("Пункт: ").strip()
    if c == "1":
        print("Привет!")
    elif c == "2":
        print("Сейчас примерно 'пора пить чай'")
    elif c == "3":
        break
    else:
        print("Нет такого пункта")

# --- Задача 12 ---
# Проси ввод чисел. Считай сумму ТОЛЬКО положительных.
# Отрицательные пропускай (continue). На 0 — остановись.
# Твой код:





total = 0
while True:
    x = int(input("Число (0 — стоп): "))
    if x == 0:
        break
    if x < 0:
        continue
    total += x
print("Сумма положительных:", total)
