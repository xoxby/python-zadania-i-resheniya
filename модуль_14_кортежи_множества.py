# --- Задача 3 ---
# Напиши функцию, которая возвращает (min, max, avg) списка.
# Вызови и распакуй результат в 3 переменные.
# Твой код:





def stats(lst):
    s = sum(lst)
    return min(lst), max(lst), s / len(lst)


mn, mx, av = stats([1, 2, 3, 4])
print(mn, mx, av)

