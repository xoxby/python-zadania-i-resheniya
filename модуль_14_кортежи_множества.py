# --- Задача 8 ---
# Два кружка: drawing = {"Марина","Женя","Света"}
#              music = {"Костя","Женя","Илья"}
# Найди: кто ходит в ОБА, кто ТОЛЬКО в рисование, кто ТОЛЬКО в музыку.
# Используй &, -, ^
# Твой код:





drawing = {"Марина", "Женя", "Света"}
music = {"Костя", "Женя", "Илья"}
print("Оба:", drawing & music)
print("Только рисование:", drawing - music)
print("Только музыка:", music - drawing)

# --- Задача 9 ---
# Удали повторы из списка, сохранив порядок (через set + цикл).
# a = [3, 1, 4, 1, 5, 9, 2, 6, 5, 3, 5]  →  [3, 1, 4, 5, 9, 2, 6]
# Твой код:





a = [3, 1, 4, 1, 5, 9, 2, 6, 5, 3, 5]
out = []
for x in a:
    if x not in out:
        out.append(x)
print(out)

# --- Задача 10 ---
# Проверь: является ли множество A подмножеством B (issubset)?
# A = {1, 2}
# B = {1, 2, 3, 4, 5}
# Твой код:





A, B = {1, 2}, {1, 2, 3, 4, 5}
