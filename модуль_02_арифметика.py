# --- Тренировка 1 (перевод времени) ---
# Дано количество секунд: total_sec = 7384
# Переведи в часы, минуты и секунды.
# Вспомни: 1 час = 3600 секунд, 1 минута = 60 секунд.
# Алгоритм:
#   chas = total_sec // 3600          ← сколько полных часов
#   ostatok = total_sec % 3600        ← что осталось после часов
#   minut = ostatok // 60             ← из остатка достаём минуты
#   sec = ostatok % 60                ← что осталось = секунды
# Ожидаемый вывод: "7384 секунд = 2 ч. 3 мин. 4 сек."
# Твой код:





total_sec = 7384
chas = total_sec // 3600
ostatok = total_sec % 3600
minut = ostatok // 60
sec = ostatok % 60
print(f"{total_sec} секунд = {chas} ч. {minut} мин. {sec} сек.")

