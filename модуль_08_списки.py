# --- Задача 5 ---
# Дан список чисел. Найди сумму, минимум и максимум (без min/max/sum).
# Пройдись циклом for.
# nums = [4, -2, 8, 1, -5, 3, 7]
# Твой код:





nums = [4, -2, 8, 1, -5, 3, 7]
s = nums[0]
mn = nums[0]
mx = nums[0]
for x in nums[1:]:
    s += x
    if x < mn:
        mn = x
    if x > mx:
        mx = x
print(s, mn, mx)

# --- Задача 6 ---
# Дан список. Посчитай количество чётных и сумму нечётных элементов.
# nums = list(range(1, 21))
# Твой код:





nums = list(range(1, 21))
evens = 0
odd_sum = 0
for x in nums:
    if x % 2 == 0:
        evens += 1
    else:
        odd_sum += x
print(evens, odd_sum)

