# --- Задача 10 ---
# Дан список nums = [3, 1, 4, 1, 5, 9, 2, 6, 5, 3, 5].
# Создай НОВЫЙ список, в котором только уникальные элементы
# (без повторов), сохраняя порядок. Без set()!
# Твой код:





nums = [3, 1, 4, 1, 5, 9, 2, 6, 5, 3, 5]
uniq = []
for x in nums:
    if x not in uniq:
        uniq.append(x)
print(uniq)

# --- Задача 11 ---
# Объедини два списка: a = [1, 2, 3] и b = [4, 5, 6].
# Выведи результат через + и через extend. В чём разница?
# Твой код:





a, b = [1, 2, 3], [4, 5, 6]
print(a + b)
c = a.copy()
c.extend(b)
print(c)
# + создаёт новый список; extend добавляет элементы в существующий

# --- Задача 12 ---
# Сравни == и is для двух одинаковых списков.
# a = [1, 2, 3]
# b = [1, 2, 3]
# c = a
# Выведи: a == b, a is b, a == c, a is c. Объясни результат комментарием.
# Твой код:





