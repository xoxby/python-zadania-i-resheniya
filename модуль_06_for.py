# --- Задача 1 ---
# Выведи числа от 1 до 10 через for и range().
# Твой код:





for i in range(1, 11):
    print(i)

# --- Задача 2 ---
# Выведи чётные числа от 2 до 20 (используй step в range).
# Твой код:





for i in range(2, 21, 2):
    print(i)

# --- Задача 3 ---
# Обратный отсчёт от 10 до 1 через range().
# Твой код:





for i in range(10, 0, -1):
    print(i)

# --- Задача 4 ---
# Посчитай сумму квадратов от 1 до n.
# Пример: n=4 → 1+4+9+16 = 30
# Твой код:





n = int(input("n: "))
s = 0
for i in range(1, n + 1):
    s += i * i
print(s)

# --- Задача 5 ---
# Выведи все делители числа n через for.
# Твой код:





n = int(input("n: "))
for d in range(1, n + 1):
    if n % d == 0:
        print(d, end=" ")
print()

# --- Задача 6 ---
# Выведи таблицу умножения для одного числа (спроси какого).
# Пример: число 7 → "7 x 1 = 7", "7 x 2 = 14", ... "7 x 10 = 70"
# Твой код:





k = int(input("Таблица для числа: "))
for i in range(1, 11):
    print(f"{k} x {i} = {k * i}")

# --- Задача 7 ---
# Переверни строку посимвольно через for (не используй [::-1]).
# Пример: "hello" → "olleh"
# Твой код:





s = input("Строка: ")
rev = ""
for ch in s:
    rev = ch + rev
print(rev)

# --- Задача 8 ---
# Посчитай количество гласных (а, е, ё, и, о, у, ы, э, ю, я) в строке.
# Спроси строку у пользователя.
# Твой код:





vowels = "аеёиоуыэюя"
s = input("Строка: ").lower()
cnt = 0
for ch in s:
    if ch in vowels:
        cnt += 1
print(cnt)

# --- Задача 9 ---
# Выведи числа от 1 до 20, но пропусти кратные 3 (continue).
# Твой код:





for i in range(1, 21):
    if i % 3 == 0:
        continue
    print(i, end=" ")
print()

# --- Задача 10 ---
# Определи, является ли число простым.
# Простое число делится только на 1 и на себя.
# Подсказка: цикл от 2 до n, если нашёл делитель — не простое (break).
# Используй for/else.
# Твой код:





n = int(input("Проверка на простоту, n>=2: "))
for d in range(2, n):
    if n % d == 0:
        print("Не простое")
        break
else:
    print("Простое")

# --- Задача 11 ---
# FizzBuzz: для чисел от 1 до 30:
# - если делится на 3 → "Fizz"
# - если делится на 5 → "Buzz"
# - если делится на 3 И на 5 → "FizzBuzz"
# - иначе → само число
# Твой код:





for i in range(1, 31):
    if i % 15 == 0:
        print("FizzBuzz")
    elif i % 3 == 0:
        print("Fizz")
    elif i % 5 == 0:
        print("Buzz")
    else:
        print(i)

# --- Задача 12 ---
# Выведи все трёхзначные числа (100-999),
# у которых сумма цифр == 10.
# Твой код:





for x in range(100, 1000):
    a, b, c = x // 100, (x // 10) % 10, x % 10
    if a + b + c == 10:
        print(x, end=" ")
print()
