# --- Задача 6 ---
# Нарисуй шахматную доску 8x8 из "#" и " ":
# # # # #
#  # # # #
# # # # #
# ...
# Подсказка: if (i + j) % 2 == 0
# Твой код:





for i in range(8):
    for j in range(8):
        print("#" if (i + j) % 2 == 0 else " ", end="")
    print()

# --- Задача 7 ---
# Выведи координаты каждой клетки поля 3x3:
# (0,0) (0,1) (0,2)
# (1,0) (1,1) (1,2)
# (2,0) (2,1) (2,2)
# Твой код:





for i in range(3):
    for j in range(3):
        print(f"({i},{j})", end=" ")
    print()

# --- Задача 8 ---
# Нарисуй равнобедренный треугольник высотой 5 (с пробелами слева):
#     *
#    ***
#   *****
#  *******
# *********
# Подсказка: пробелы = h-i-1, звёздочки = 2*i+1
# Твой код:





h = 5
for i in range(h):
    spaces = " " * (h - i - 1)
    stars = "*" * (2 * i + 1)
    print(spaces + stars)

# --- Задача 9 ---
# Нарисуй ромб высотой 7 (нечётное число):
#    *
#   ***
#  *****
# *******
#  *****
#   ***
#    *
# Твой код:





h = 7
mid = h // 2
for i in range(mid + 1):
    print(" " * (mid - i) + "*" * (2 * i + 1))
for i in range(mid - 1, -1, -1):
    print(" " * (mid - i) + "*" * (2 * i + 1))

# --- Задача 10 ---
# Нарисуй "ёлочку" из звёздочек: 3 треугольника друг над другом,
# каждый следующий на 2 символа шире. Придумай сам форму.
# Твой код:





widths = [3, 5, 7]
for w in widths:
    h = (w + 1) // 2
    for i in range(h):
        print(" " * (h - i - 1) + "*" * (2 * i + 1))
    print()
