# --- Задача 2 ---
# Нарисуй ПУСТОЙ прямоугольник 6x4:
# ******
# *    *
# *    *
# ******
# Подсказка: if условие на первую/последнюю строку и первый/последний столбец
# Твой код:





h, w = 4, 6
for r in range(h):
    for c in range(w):
        if r in (0, h - 1) or c in (0, w - 1):
            print("*", end="")
        else:
            print(" ", end="")
    print()

